# Chapter 2: Strings

# Lets right a name in lowercase and then use the title() method to capitalize it
name = "ada lovelace"
print (name.title()) # Look how the title() method capitalizes each word in the string

# We can also use upper() and lower() methods to change the case of the string
name = "Ada Lovelace"
print (name.upper()) # This will print the name in all uppercase letters
print (name.lower()) # This will print the name in all lowercase letters